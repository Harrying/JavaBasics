package com.whr;

public class StringDema {

	public static void main(String[] args) {
		// 定义一空字符串
		String s1="";
		System.out.println("s1="+s1);
		// 定义一个hello的字符串
		String s2="Hello";
		System.out.println("s2="+s2);
		//  定义一个含有Unicode字符的字符串
	  String s3="A\u005d\u005fB";
	  System.out.println("s3="+s3);

	}

}
